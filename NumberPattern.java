public class NumberPattern {
    public static void main(String[] args) {
        int n = 5; // Number of rows
        
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
        
        // This line should be inside the main method
        System.out.println("By PRIYANSHI AGRAWAL 23SCSE1690056");
    }
}

